import 'dart:ui' as ui;

import 'package:flutter/services.dart';

/// Loads a test image for demo screens.
///
/// Tries the bundled asset first, then falls back to a programmatically
/// generated JPEG so the demo works in any environment.
Future<Uint8List> loadTestImage() async {
  // Try bundled asset
  try {
    final data = await rootBundle.load('assets/sample.jpg');
    return data.buffer.asUint8List();
  } catch (_) {
    // No asset available — generate one
  }

  return _generateTestImage();
}

/// Generates a test image programmatically using dart:ui.
///
/// Creates a 2048x1536 image (3.1 MP, typical phone photo) with gradients
/// and shapes so compression demos produce meaningful results.
Future<Uint8List> _generateTestImage() async {
  const width = 2048;
  const height = 1536;

  final recorder = ui.PictureRecorder();
  final canvas = ui.Canvas(recorder);

  // Background gradient
  final bgPaint =
      ui.Paint()
        ..shader = ui.Gradient.linear(
          ui.Offset.zero,
          const ui.Offset(2048, 1536),
          [
            const ui.Color(0xFF1A237E),
            const ui.Color(0xFF0D47A1),
            const ui.Color(0xFF00838F),
            const ui.Color(0xFF00695C),
          ],
          [0.0, 0.33, 0.66, 1.0],
        );
  canvas.drawRect(const ui.Rect.fromLTWH(0, 0, 2048, 1536), bgPaint);

  // Add overlapping circles with radial gradients for texture
  final colors = [
    const ui.Color(0x80FF6F00),
    const ui.Color(0x80E91E63),
    const ui.Color(0x8000BCD4),
    const ui.Color(0x804CAF50),
    const ui.Color(0x80FF5722),
    const ui.Color(0x809C27B0),
  ];
  final positions = [
    const ui.Offset(512, 384),
    const ui.Offset(1536, 512),
    const ui.Offset(1024, 1024),
    const ui.Offset(384, 1152),
    const ui.Offset(1664, 1152),
    const ui.Offset(1024, 512),
  ];
  final radii = [307.0, 256.0, 358.0, 230.0, 282.0, 333.0];

  for (var i = 0; i < colors.length; i++) {
    final paint =
        ui.Paint()
          ..shader = ui.Gradient.radial(positions[i], radii[i], [
            colors[i],
            colors[i].withAlpha(0),
          ]);
    canvas.drawCircle(positions[i], radii[i], paint);
  }

  // Add some rectangles for edge detail
  for (var x = 0; x < width; x += 80) {
    for (var y = 0; y < height; y += 80) {
      final paint =
          ui.Paint()
            ..color = ui.Color.fromARGB(
              20,
              (x * 3 + y) % 256,
              (y * 2 + x) % 256,
              (x + y) % 256,
            )
            ..style = ui.PaintingStyle.stroke
            ..strokeWidth = 1.0;
      canvas.drawRect(
        ui.Rect.fromLTWH(x.toDouble(), y.toDouble(), 60, 60),
        paint,
      );
    }
  }

  // Add text-like shapes (small rectangles) for detail
  final detailPaint = ui.Paint()..color = const ui.Color(0x40FFFFFF);
  for (var y = 640; y < 896; y += 16) {
    final w = 256.0 + (y % 154);
    canvas.drawRect(
      ui.Rect.fromLTWH(1024 - w / 2, y.toDouble(), w, 8),
      detailPaint,
    );
  }

  final picture = recorder.endRecording();
  final image = await picture.toImage(width, height);
  final byteData = await image.toByteData(format: ui.ImageByteFormat.png);

  if (byteData == null) {
    return syntheticJpeg();
  }

  return byteData.buffer.asUint8List();
}

